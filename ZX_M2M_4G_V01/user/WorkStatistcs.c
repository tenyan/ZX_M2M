/********************************重型基本信息数据-TCB******************************/
//通过CAN总线采集数据组成TLV,可以通过TLV函数返回长度判断是否真正采集到数据
//通信协议版本 
uint8 TCB_BulidTlvMsg_3001(uint *pBuf)
{
    *pBuf++ = 0x30;
	*pBuf++ = 0x01;
	*pBuf++ = 0;
	*pBuf++ = 2;

    return 4+2;
}

/********************************重型工况采集数据-TCW******************************/

//力限器工况信息（单缸）
uint8 TCW_BulidTlvMsg_A5A0(uint *pBuf)
{
    *pBuf++ = 0xA5;
	*pBuf++ = 0xA0;
	*pBuf++ = 0;
	*pBuf++ = 80;

    return 80+2;
}

/********************************重型故障码-TCD******************************/

//上车故障
uint8 TCD_BulidTlvMsg_A507(uint *pBuf)
{
    *pBuf++ = 0xA5;
	*pBuf++ = 0x07;
	*pBuf++ = 0;
	*pBuf++ = 80;

    return 4+2;
}

